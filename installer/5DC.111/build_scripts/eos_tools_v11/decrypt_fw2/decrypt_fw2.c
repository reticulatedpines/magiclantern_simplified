#define CRYPT1_SIZE (sizeof(crypt1)/sizeof(crypt1[0]))
#define CRYPT2_SIZE (sizeof(crypt2)/sizeof(crypt2[0]))

int main(int argc, char *argv[])
{
  FILE *in;
  FILE *out;
  int i = 0, j = 0, val;

  if (argc != 3) {
    printf("Usage: decrypt inputfile outfile\n");
    return -1;
  }

  if ((in = fopen(argv[1], "rb")) == NULL) {
    printf("Cant't open file name %s\n", argv[1]);
    return -1;
  }

  if ((out = fopen(argv[2], "wb")) == NULL) {
    printf("Cant't open file name %s\n", argv[2]);
    fclose(in);
    return -1;
  }

#if 0
  fseek(in, 32, SEEK_SET);	//Skip the first 32 bytes for 30D they are not encrypted
#else
  for(i=0; i < 0x20 ;i++) {
    if((val = fgetc(in)) != EOF){
      fputc(val, out);
    }
  }
#endif

i=0;

  while ((val = fgetc(in)) != EOF) {
    fputc(val ^ crypt1[i] ^ crypt2[j], out);
    i++;
    j++;
    if (i >= CRYPT1_SIZE) i=0;
    if (j >= CRYPT2_SIZE) j=0;
  }
  fclose(out);
  fclose(in);
  return 0;
}
